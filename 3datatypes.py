# Popular Python variable's types
# For get type of variable,Python has the type function

# String
name = "Morteza"
print (name)
print (type(name))

# Numbers -> 1.Integer
weight = 1
print (weight)
print (type(weight))

# 2.Float
height = 1.1
print (height)
print (type(height))

# Boolean
true_or_false = True
print (true_or_false)
print (type(true_or_false))

# List
my_list = [1, 'A', 2, 'B']
print (my_list)
print (type(my_list))

# Dictionary
my_dict = {'A': 1, "B": 2, "C": 3, "D": 4}
print (my_dict)
print (type(my_dict))

# Tuple
my_tuple = (1, 2, 3, 4, 5)
print (my_tuple)
print (type(my_tuple))

# Set
my_set = {'a', 'b', "c", 1, 9}
print (my_set)
print (type(my_set))